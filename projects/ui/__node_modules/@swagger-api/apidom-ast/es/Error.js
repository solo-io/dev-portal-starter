import stampit from 'stampit';
import Node from "./Node.js";
const Error = stampit(Node, {
  statics: {
    type: 'error'
  },
  props: {
    value: null,
    isUnexpected: false
  },
  init({
    value = null,
    isUnexpected = false
  } = {}) {
    this.value = value;
    this.isUnexpected = isUnexpected;
  }
});
export default Error;