import stampit from 'stampit';
import { head } from 'ramda';
import JsonNode from "./JsonNode.js";
const JsonDocument = stampit(JsonNode, {
  statics: {
    type: 'document'
  },
  methods: {
    // @ts-ignore
    get child() {
      // @ts-ignore
      return head(this.children);
    }
  }
});
export default JsonDocument;