import stampit from 'stampit';
import { head } from 'ramda';
import Node from "./Node.js";
const ParseResult = stampit(Node, {
  statics: {
    type: 'parseResult'
  },
  methods: {
    // @ts-ignore
    get rootNode() {
      // @ts-ignore
      return head(this.children);
    }
  }
});
export default ParseResult;