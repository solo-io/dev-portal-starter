import stampit from 'stampit';
import Node from "../../Node.js";
const YamlNode = stampit(Node, {
  props: {
    anchor: null,
    tag: null,
    style: null,
    styleGroup: null
  },
  init({
    anchor = null,
    tag = null,
    style = null,
    styleGroup = null
  } = {}) {
    this.anchor = anchor;
    this.tag = tag;
    this.style = style;
    this.styleGroup = styleGroup;
  }
});
export default YamlNode;