import stampit from 'stampit';
import { BREAK } from '@swagger-api/apidom-core';
import SpecificationVisitor from "../../SpecificationVisitor.js";
import FallbackVisitor from "../../FallbackVisitor.js";
import { isSecurityRequirementLikeElement } from "../../../predicates.js";
import ServerSecurityElement from "../../../../elements/nces/ServerSecurity.js";
const SecurityVisitor = stampit(SpecificationVisitor, FallbackVisitor, {
  init() {
    this.element = new ServerSecurityElement();
  },
  methods: {
    ArrayElement(arrayElement) {
      arrayElement.forEach(item => {
        if (isSecurityRequirementLikeElement(item)) {
          const serverElement = this.toRefractedElement(['document', 'objects', 'SecurityRequirement'], item);
          this.element.push(serverElement);
        } else {
          this.element.push(item);
        }
      });
      this.copyMetaAndAttributes(arrayElement, this.element);
      return BREAK;
    }
  }
});
export default SecurityVisitor;