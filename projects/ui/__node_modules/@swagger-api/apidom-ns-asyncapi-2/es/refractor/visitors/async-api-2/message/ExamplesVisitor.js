import stampit from 'stampit';
import { isObjectElement, BREAK } from '@swagger-api/apidom-core';
import SpecificationVisitor from "../../SpecificationVisitor.js";
import FallbackVisitor from "../../FallbackVisitor.js";
import MessageExamplesElement from "../../../../elements/nces/MessageExamples.js";
const ExamplesVisitor = stampit(SpecificationVisitor, FallbackVisitor, {
  init() {
    this.element = new MessageExamplesElement();
  },
  methods: {
    ArrayElement(arrayElement) {
      arrayElement.forEach(item => {
        let element;
        if (isObjectElement(item)) {
          element = this.toRefractedElement(['document', 'objects', 'MessageExample'], item);
        } else {
          element = item.clone();
        }
        this.element.push(element);
      });
      this.copyMetaAndAttributes(arrayElement, this.element);
      return BREAK;
    }
  }
});
export default ExamplesVisitor;